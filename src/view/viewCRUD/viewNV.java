    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        int index = tblNhanVien.getSelectedRow();
        lbID.setText(tblNhanVien.getValueAt(index, 0).toString());
        txtMa.setText(tblNhanVien.getValueAt(index, 1).toString());
        txtTen.setText(tblNhanVien.getValueAt(index, 2).toString());
        if(tblNhanVien.getValueAt(index, 3).toString().equalsIgnoreCase("Nam")){
            rdNam.setSelected(true);
        }
        if(tblNhanVien.getValueAt(index, 3).toString().equalsIgnoreCase("Nữ")){
            rdNu.setSelected(true);
        }
        txtSDT.setText(tblNhanVien.getValueAt(index, 4).toString());
        txtCCCD.setText(tblNhanVien.getValueAt(index, 5).toString());
        txtNgaySinh.setText(tblNhanVien.getValueAt(index, 6).toString());
        txtDiaChi.setText(tblNhanVien.getValueAt(index, 7).toString());
        txtEmail.setText(tblNhanVien.getValueAt(index, 8).toString());
        if(tblNhanVien.getValueAt(index, 9).toString().equalsIgnoreCase("Nhân viên")){
            rdNhanVien.setSelected(true);
        }
        if(tblNhanVien.getValueAt(index, 9).toString().equalsIgnoreCase("Quản Lí")){
            rdQuanLi.setSelected(true);
        }
        if(tblNhanVien.getValueAt(index, 10).toString().equalsIgnoreCase("Đang làm")){
            rdDangLam.setSelected(true);
        }
        if(tblNhanVien.getValueAt(index, 10).toString().equalsIgnoreCase("Đã Nghỉ")){
            rdDaNghi.setSelected(true);
        }
    }//GEN-LAST:event_tblNhanVienMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        NhanVien nv = new NhanVien();
        nv.setMa(txtMa.getText().trim());
        nv.setTen(txtTen.getText().trim());
        int gioiTinh = 0;
        if(rdNam.isSelected()==true)
            gioiTinh = 1;
        nv.setGioiTinh(gioiTinh);
        nv.setSdt(txtSDT.getText().trim());
        nv.setCccd(txtCCCD.getText().trim());
        nv.setNgaySinh(txtNgaySinh.getText().trim());
        nv.setDiaChi(txtDiaChi.getText().trim());
        nv.setEmail(txtEmail.getText().trim());
        int chucVu = 0;
        if(rdNhanVien.isSelected()==true)
            chucVu = 1;
        nv.setChucVu(chucVu);
        int trangThai = 0;
        if(rdDangLam.isSelected()==true)
            trangThai =1;
        nv.setTrangThai(trangThai);
        service.Them(nv);
        JOptionPane.showMessageDialog(this, "Thêm thành công");
        LoadDataTable(service.getList());
        Clear();
        
    }//GEN-LAST:event_btnThemActionPerformed

