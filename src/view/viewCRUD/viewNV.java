    public void LoadDataTable(ArrayList<QLNhanVien> list){
         defaultTableModel = (DefaultTableModel)tblNhanVien.getModel();
         defaultTableModel.setRowCount(0);
         for(QLNhanVien nv : list){
             defaultTableModel.addRow(new Object[]{
                 nv.getId(), nv.getMa(),nv.getTen(),(nv.getGioiTinh()==1)?"Nam":"Nữ",nv.getSdt(),
                 nv.getCccd(), nv.getNgaySinh(),nv.getDiaChi(), nv.getEmail(),(nv.getChucVu()==1)?"Nhân Viên":"Quản Lí",
                 (nv.getTrangThai()==1)?"Đang làm":"Đã Nghỉ"
             });
         }
    }
