    private void taidulieu() {
        DefaultTableModel model = (DefaultTableModel) this.tblHDCT.getModel();
        ArrayList<HoaDonCTView> list = (ArrayList<HoaDonCTView>) this.service.getAll();
        xoaRongTable();
        for (HoaDonCTView x : list) {
            Object[] rowData = {
                x.getIdHD(), x.getId(), x.getIdSPCT(), x.getSoluong(), x.getDonGia()
            };
            model.addRow(rowData);
        }
    }

    private HoaDonCTView getForm() {
        try {
            int soluong = Integer.parseInt(txtSoLuong.getText());
            int dongia = Integer.parseInt(txtDonGia.getText());
            HoaDonCTView hv = new HoaDonCTView(1, 2, 3, soluong, dongia);
            return hv;
        } catch (NumberFormatException e) {
            e.printStackTrace();
        }
        return null;
    }

    private void xoaRongTable() {
        DefaultTableModel model = (DefaultTableModel) this.tblHDCT.getModel();
        while (tblHDCT.getRowCount() > 0) {
            model.removeRow(0);
        }
    }
