    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        try {
            int row = tblHDCT.getSelectedRow();
            if (row < 0) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn hóa đơn CT cần xóa");
                return;
            }
            Object obj = tblHDCT.getValueAt(row, 0);
            int id_hd = Integer.parseInt(obj.toString());
            service.delete(id_hd);
            this.taidulieu();
            JOptionPane.showMessageDialog(this, "Xóa hóa đơn thành công");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Xóa hóa đơn thất bại");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

   
