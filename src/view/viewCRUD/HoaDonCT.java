    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed

        try {
            int row = tblHDCT.getSelectedRow();
            if (row < 0) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn hóa đơn cần xóa");
                return;
            }
            Object obj = tblHDCT.getValueAt(row, 0);
            int id_hd = Integer.parseInt(obj.toString());
            HoaDonCTView hd = getForm();
            if (this.service.update(id_hd, hd)) {
                this.taidulieu();
                JOptionPane.showMessageDialog(this, "Sửa HDCT thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Sửa HDCT thất bại");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Sửa HDCT thất bại");
        }

    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            int chon = JOptionPane.showConfirmDialog(this, "Xác nhận thêm");
            if (chon == JOptionPane.YES_OPTION) {
                if (service.add(this.getForm())) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    this.taidulieu();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Hủy thêm thành công");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnThemActionPerformed

